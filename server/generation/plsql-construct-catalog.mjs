function escapeRegex(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function word(token) {
  return new RegExp(`\\b${escapeRegex(token)}\\b`, 'i');
}

function phrase(text) {
  return new RegExp(
    text
      .trim()
      .split(/\s+/)
      .map((part) => escapeRegex(part))
      .join('\\s+'),
    'i',
  );
}

function call(name) {
  return new RegExp(`\\b${escapeRegex(name)}\\s*\\(`, 'i');
}

